<h1>Qui suis-je ?</h1>

<p>
	Benjamin TATARD<br> aussi connu sous le nom du "Pr. Blackflames" était encore un jeune garçon
lorsqu'il découvrit ses premières fêtes médiévales et l’univers de la fantaisy.<br>
 Enchanté par ces derniers, il
entreprit quelques années plus tard une formation au sein de l'école des "Compagnons du Devoir" au
sein de laquelle il espérait apprendre les techniques de la forge dans l'objectif de devenir Coutelier
d'Art. Malgré deux années d'apprentissages, peu nombreuses étaient les occasions pour apprendre la
forge mais cela ne l'empêcha pas de s'exercer en autodidacte sur ses premières "lames".<br> 
C'est ainsi qu'un
apprenti maroquinier lui échangea un bracelet en cuir contre l'un de ses couteaux et l'invita également à
venir dans son atelier de maroquinerie: le « Prof » venait de faire ses premiers pas dans le travail du cuir.<br>
Après l'obtention de son C.A.P, le Prof commença ses petites créations en autodidacte: Petits accessoires
et pièces d'armure en cuir, armes en mousse et latex ou couteaux simples en acier ; le mécanisme du
bouche à oreilles se mit en place petit à petit.<br>
 Les gens venaient à sa rencontre pour découvrir son travail
avec un intérêt sans cesse grandissant. La réception des premières commandes marqua le véritable départ
dans son artisanat et c'est ainsi qu'il regroupa ses créations sous le nom de:
L'Atelier des Flammes Noires
(Atelier: Lieu de création artisanal/ Flammes: Feux, Forge, Création/ Noires: Fumée de Charbon, vieille
machine/ Flammes Noires: Fantaisie)<br><br>
Avec le temps, il se consacra principalement au travail du cuir mais aussi et surtout à l'art de transformer
la matière: l'artisanat à travers le "recyclage".
En effet, la majorité de ses réalisations (~65%) sont créées à partir de matériaux issus de la "récup": bois,
cuir, plastique ou encore métal ; aussi bien des objets que de la matière première, trouvés et transformés
afin de leur donner une nouvelle vie, proposant ainsi des pièces toujours plus originales et Uniques.<br>
En parallèle, il découvrit d'autres univers de la création de costume: Le "GN" (jeux de rôle Grandeur
Nature), la pratique du "Cosplay" (Costume-Playing) ou encore le Steampunk (Uchronie Rétrofuturiste).<br>
Ce dernier fut d'ailleurs son univers de prédilection durant plusieurs années, lui permettant de se faire
reconnaitre en tant que créateur sous le pseudonyme du "Professeur Blackflames" (Cela inspira également
le nom de son atelier ainsi que son logo)
Officiellement auto-entrepreneur depuis 2013, il décida de transformer sa passion en métier en prenant le
statut d’Artisan. Ne cessant de construire sa propre expérience, il peaufine sa technique et nourrit son
imagination au gré des opportunités et des rencontres durant les nombreux festivals et évènements
français (et internationaux). D'abord en tant que visiteur puis, finalement, en tant qu’exposant ou encore
sur la scène, cela fait bientôt quelques années que le Prof partage son travail à travers ses costumes/
cosplays et ses nombreuses réalisations surprenantes dont il a le secret.
